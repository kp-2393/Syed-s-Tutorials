	"id"	INTEGER NOT NULL,
	"FirstName"	TEXT NOT NULL,
	"lastName"	INTEGER NOT NULL,
	"email"	TEXT NOT NULL UNIQUE,
	"languages known"	TEXT NOT NULL,
	PRIMARY KEY("1" AUTOINCREMENT)
);

