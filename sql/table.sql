CREATE TABLE "students" (
	"id"	INTEGER NOT NULL,
	"firstName"	TEXT NOT NULL,
	"lastName"	TEXT NOT NULL,
	"email"	TEXT NOT NULL UNIQUE,
	"classType"	TEXT NOT NULL,
	"course"	TEXT NOT NULL,
	PRIMARY KEY("id" AUTOINCREMENT)
);

